    private void btnOkNgayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkNgayActionPerformed
        String ngayInput = txtNgay.getText().trim();

        if (!isValidDate(ngayInput)) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đúng định dạng ngày yyyy-MM-dd", "Lỗi định dạng", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            StatisticsBUS thongKeBUS = new StatisticsBUS();
            List<Object[]> data = thongKeBUS.getRevenueBySpecificDate(ngayInput);

            DefaultTableModel model = (DefaultTableModel) tableDoanhThuNgay.getModel();
            for (Object[] row : data) {
                model.addRow(row);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Đã xảy ra lỗi: " + ex.getMessage());
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnOkNgayActionPerformed

    private void btnOkKhoangNgayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkKhoangNgayActionPerformed
        String tuNgayInput = txtTuNgay.getText().trim();
        String denNgayInput = txtDenNgay.getText().trim();

        if (!isValidDate(tuNgayInput) || !isValidDate(denNgayInput)) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đúng định dạng ngày yyyy-MM-dd cho cả Từ ngày và Đến ngày", "Lỗi định dạng", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            StatisticsBUS thongKeBUS = new StatisticsBUS();
            List<Object[]> data = thongKeBUS.getRevenueByDateRange(tuNgayInput, denNgayInput);

            DefaultTableModel model = (DefaultTableModel) tableDoanhThuKhoangNgay.getModel();
            for (Object[] row : data) {
                model.addRow(row); 
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Đã xảy ra lỗi: " + ex.getMessage());
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnOkKhoangNgayActionPerformed

